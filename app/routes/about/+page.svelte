    <h1 class="page-title">About</h1>

    <p>
      Indiemusi.ch is a website started by  <a href="https://hilk.eu" target="_blank" rel="noopener">musician</a> and 'tech sis'
      <a href="https://mu.social/profile/hilk.eu" target="_blank" rel="noopener">Hilke</a>.
    </p>

    <p>
      With a couple of friends from the music scene, Hilke formed an ad hoc group of people that
      wanted to organise something to discuss the state of affairs for musicians in the current
      political climate, and more particularly about the role of social media, an essential tool for
      musicians to be heard. They organised a couple of events to feel the temperature in the music
      community, to discuss and brainstorm about strategies and new ideas.
    </p>

    <p>
      Hilke felt the need to gather some information around this on a website and what you see here
      is the result thereof. It is her aim to continue working on these topics by building more
      community and collaboration. However, all the ideas expressed here are her own and don't
      necessarily represent everyone that participated in the "Fighting Broligarchy" events.
    </p>
